#include "holberton.h"
/**
 * print_diagonal -  prints a diagonal lines
 * @n: is number of time the line prints
 * Return: 0 always
 */
void print_diagonal(int n)
{
	int i;
	int j;

	if (n > 0)
		for (i = 1; i <= n; i++)
		{
			for (j = 1; j <= i ; j++)
			{
				_putchar(' ');
			}
			_putchar('\\');
			if (j == i)
				break;
			_putchar('\n');
		}
	else if (n <= 0)
		_putchar('\n');
}
